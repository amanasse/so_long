unsigned int	count_u(unsigned int nb)
{
	unsigned int	n;

	n = 0;
	if (nb == 0)
		return (1);
	while (nb > 9)
	{
		nb = nb / 10;
		n++;
	}
	if (nb > 0 && nb < 10)
		n++;
	return (n);
}
